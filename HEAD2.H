// create a header file with name head2.h  to calculate the sum of 2 integers and product of 3 integers and the average of 4 floting point numbers

int sum(int x,int y)
{
 return(x+y);
}
int prod(int x,int y,int z)
{
 return(x+y+z);
}
int avg(float x,float y,float z,float w)
{
 return((x+y+z+w)/4.0);
}